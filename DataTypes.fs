printf "%i\n" my_int

let my_float:float = 50.50
printf "%.2f\n" my_float

let my_char:char = 'F'
printf "This is %c#\n" my_char

let my_string:string = "This is F#\n"
printf "%s" my_string

let my_bool:bool = true
printf "True: %b\n" (my_bool)